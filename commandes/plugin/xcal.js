import config from '../../config.cjs';

const report = async (m, gss) => {
  try {
    const prefix = config.PREFIX;
    const cmd = m.body.startsWith(prefix) ? m.body.slice(prefix.length).split(' ')[0].toLowerCase() : '';
    const text = m.body.slice(prefix.length + cmd.length).trim();

    const validCommands = ['cal', 'calculater', 'calc'];
    if (!validCommands.includes(cmd)) return;

    if (!text) {
      return m.reply(`✏️ *Enter an expression to flex my brain*\n\n💡 _Example:_ \n.calc (5 + π) × 2`);
    }

    let raw = text
      .replace(/[^0-9\-+*/^πe().×÷piPI]/gi, '')
      .replace(/×/g, '*')
      .replace(/÷/g, '/')
      .replace(/\^/g, '**')
      .replace(/π|pi/gi, 'Math.PI')
      .replace(/e/gi, 'Math.E');

    let display = text
      .replace(/pi|π/gi, 'π')
      .replace(/e/gi, 'e')
      .replace(/\*/g, '×')
      .replace(/\//g, '÷')
      .replace(/\^/g, '^');

    let result = (new Function('return ' + raw))();

    if (isNaN(result)) throw new Error("⚠️ *Invalid expression!*\n\nTry something like:\n.calc (4 + π) × 2");

    const styled = `
╭━━━「 ᴄᴀʟᴄᴜʟᴀᴛᴏʀ 」━━━
│
│ 🧠 *Expression:*
│     ${display}
│
│ ✨ *Answer:*
│    *${result}*
│
╰───────────────
ᴘᴏᴡᴇʀᴇᴅ ʙʏ ʟᴏʀᴅ ᴊᴏᴇʟ
    `;

    m.reply(styled);
  } catch (error) {
    console.error('Calc Error:', error);
    m.reply(`❌ *Oops! Error in your expression.*\n\n💡 _Try something like:_\n.calc 9 ÷ (2 + π)`);
  }
};

